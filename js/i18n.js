  var MARQUEE_REPEAT = 3;

  function setLang(lang) {
    var dict = I18N[lang];
    if (!dict) return;

    document.querySelectorAll("[data-i18n]").forEach(function (el) {
      var value = dict[el.getAttribute("data-i18n")];
      if (value === undefined) return;
      // values are static strings from this file; <br> only appears in display headings
      if (value.indexOf("<br>") !== -1) {
        el.innerHTML = value;
      } else {
        el.textContent = value;
      }
    });

    document.querySelectorAll("[data-i18n-marquee]").forEach(function (el) {
      var phrase = dict[el.getAttribute("data-i18n-marquee")];
      if (phrase !== undefined) el.textContent = new Array(MARQUEE_REPEAT + 1).join(phrase);
    });

    document.querySelectorAll(".lang-btn").forEach(function (btn) {
      btn.classList.toggle("active", btn.getAttribute("data-lang") === lang);
    });

    document.documentElement.lang = lang;
    document.title = dict["meta.title"];

    var desc = document.querySelector('meta[name="description"]');
    if (desc && dict["meta.desc"]) desc.setAttribute("content", dict["meta.desc"]);

    try { localStorage.setItem("zubayr-lang", lang); } catch (e) { /* private mode */ }
  }

  document.querySelectorAll(".lang-btn").forEach(function (btn) {
    btn.addEventListener("click", function () {
      setLang(btn.getAttribute("data-lang"));
    });
  });

  var saved = null;
  try { saved = localStorage.getItem("zubayr-lang"); } catch (e) { /* private mode */ }
  setLang(saved && I18N[saved] ? saved : "uz");
